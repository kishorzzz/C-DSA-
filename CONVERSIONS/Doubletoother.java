package CONVERSIONS;

public class Doubletoother {
    public static void main(String[] args) {
        double a=23.00;
        //Double to String
        String b=Double.toString(a);
        System.out.println(b);
        //Double to Integer
        int c=(int)a;
        System.out.println(c);
        //Double to Float
        float d=(float)a;
        System.out.println(d);
    }
}
