package CONVERSIONS;

public class Floattoother {
    public static void main(String[] args) {
        float a=1.2f;
        //Float to Integer 
        int b=(int)a;
        System.out.println(b);
        //Float to String 
        String c=Float.toString(a);
        System.out.println(c);
        //Float to doble direct (Implicit Expresion )
        double d=a;
        System.out.println(d);
    }
}
