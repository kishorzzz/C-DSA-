package CONVERSIONS;

public class IntegertoOther {
    public static void main(String[] args) {
        int number=23;
        //Integer to String 
        String a=Integer.toString(number);
        System.out.println(a);
        // Integer to Float 
        float ele=(float)number;
        System.out.println(ele);
        //Integer to Double 
        double element =(double)number;
        System.out.println(element);
    }
}
